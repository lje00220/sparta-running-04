/**
 * [02.js]
 *
 * - 주어진 todos 배열에 새 할 일 (title)을 추가해야 합니다.
 * - createTodo 함수를 작성하여 다음을 수행하세요:
 *   1) 고유한 id 값을 만들고
 *   2) title 값을 가지며
 *   3) completed 속성을 false로 설정한 todo 객체를 생성합니다.
 *   4) 기존 todos 배열 뒤에 새 객체를 추가한 배열을 반환합니다.
 *
 * @param {Array} todos
 * @param {string} title
 * @returns {Array} 새로운 todos 배열
 */
function createTodo(todos, title) {
  const newTodo = {
    id: Date.now(),
    title: title,
    completed: false,
  };
  todos = [...todos, newTodo];
  return todos;
}

// export 수정 불가
export { createTodo };
